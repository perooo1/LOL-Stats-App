package com.plenart.lolstats.LOLPOJO.Runes;

public class Rune {
    private String path;
    private String keystone;
    private String name;
    private String description;
    private String rune_url;

    public String getPath() {
        return path;
    }

    public String getKeystone() {
        return keystone;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getRune_url() {
        return rune_url;
    }
}
